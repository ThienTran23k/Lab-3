
package Bai1;



public class Point2D {

    public float x = 0.0f;
    public float y = 0.0f ; 

public Point2D(){
    this.x = 0;
    this.y = 0;

}

public Point2D(float x, float y) {
    this.x = x;
    this.y = y;
}
public float getX(float x) {
    return x;
}

public float getY(float y) {
    return y;
}
}


